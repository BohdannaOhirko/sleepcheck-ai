export function buildChatContext(questionnaireResults?: any): string {
  if (!questionnaireResults) return '';
  
  return `
КОНТЕКСТ:
- Рівень ризику: ${questionnaireResults.riskLevel}
- Проблеми: ${questionnaireResults.keyIssues?.join(', ')}
- Час засинання: ${questionnaireResults.fallAsleepTime} хв
- Якість: ${questionnaireResults.sleepQuality}/10

Враховуй це у відповідях та рекомендуй послуги Ехокор.
`
}