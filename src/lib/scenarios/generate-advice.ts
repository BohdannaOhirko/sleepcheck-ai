export function generatePersonalizedAdvice(
  answers: Record<string, unknown>,
  bmi: number
): string[] {
  const advice: string[] = [];

  // Позиція сну
  if (answers['snoring'] || answers['breathing-pauses']) {
    advice.push('💤 Спробуйте спати на боці — це зменшує хропіння та зупинки дихання. Використовуйте спеціальну подушку або тенісний м\'ячик на спині піжами.');
  }

  // Вага
  if (bmi >= 30) {
    advice.push(`⚖️ Зниження ваги — ключовий фактор. При ІМТ ${bmi.toFixed(1)} схуднення на 5-10 кг суттєво зменшить симптоми.`);
  } else if (bmi >= 25) {
    advice.push('⚖️ Підтримуйте здорову вагу. Навіть невелике збільшення може погіршити симптоми.');
  }

  // Алкоголь
  if ((answers['alcohol'] as number || 0) >= 3) {
    advice.push('🍷 Уникайте алкоголю за 3-4 години до сну. Він розслаблює м\'язи горла і погіршує апное.');
  }

  // Куріння
  if (answers['smoking']) {
    advice.push('🚭 Кидайте палити. Куріння збільшує ризик апное у 3 рази через набряк дихальних шляхів.');
  }

  // Час засинання
  if ((answers['sleep-onset'] as number || 0) >= 30) {
    advice.push('🛏️ При труднощах із засинанням: лягайте тільки при сонливості, уникайте екранів за годину до сну, тримайте спальню прохолодною та темною.');
  }

  // Режим сну
  if ((answers['sleep-time'] as number || 23) >= 24 || (answers['sleep-duration'] as number || 7) < 6) {
    advice.push('⏰ Встановіть регулярний режим: лягайте та вставайте в один час, навіть у вихідні. Спіть 7-9 годин.');
  }

  // Фізична активність
  if ((answers['physical-activity'] as number || 5) <= 3) {
    advice.push('🏃 Збільшіть активність — 30 хвилин вправ 5 разів на тиждень покращують сон. Але не тренуйтесь за 3 години до сну.');
  }

  // Носове дихання
  if (answers['mouth-breathing']) {
    advice.push('👃 При диханні ротом зверніться до ЛОРа — можливі проблеми з носом (викривлення перегородки, поліпи, алергія).');
  }

  // Гіпертензія
  if (answers['hypertension']) {
    advice.push('❤️ Контролюйте тиск щодня. Лікування апное може знизити його на 5-10 мм рт.ст.');
  }

  return advice;
}