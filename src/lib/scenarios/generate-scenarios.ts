import { CriticalSymptom, PossibleCondition, ScenariosData } from '@/types/scenarios';

export function generateScenarios(
  symptoms: CriticalSymptom[],
  conditions: PossibleCondition[],
  answers: Record<string, unknown>,
  bmi: number
): ScenariosData {
  const hasApnea = conditions.some(c => c.name.includes('апное') && c.probability !== 'можлива');
  const hasSevere = symptoms.filter(s => s.severity === 'critical').length >= 1;
  
  return {
    withTreatment: {
      timeline: '3-6 місяців при правильному лікуванні',
      changes: [
        'Значне покращення якості сну та відпочинку вранці',
        'Зменшення або зникнення денної сонливості',
        'Покращення концентрації та пам\'яті',
        'Стабілізація артеріального тиску',
        'Підвищення енергії та працездатності',
        'Покращення настрою',
        hasApnea ? 'Зменшення ризику серцево-судинних ускладнень на 30-50%' : '',
        bmi >= 30 ? 'При схудненні на 10% — зменшення симптомів на 50%' : ''
      ].filter(Boolean),
      benefits: [
        'Безпечне водіння та робота',
        'Покращення стосунків (менше хропіння)',
        'Більше енергії для життя',
        'Зниження ризику інфаркту та інсульту'
      ]
    },
    withoutChanges: {
      timeline: '1-2 роки без лікування',
      changes: [
        'Поступове погіршення якості сну',
        'Наростання денної втоми та сонливості',
        'Погіршення пам\'яті та концентрації',
        hasSevere ? 'Підвищення артеріального тиску' : 'Ризик розвитку гіпертензії',
        'Збільшення ваги через порушення метаболізму',
        'Погіршення настрою, можлива депресія'
      ],
      risks: [
        'Підвищений ризик ДТП (у 2-7 разів)',
        'Ризик серцевих захворювань +30%',
        'Можливе погіршення діабету'
      ]
    },
    worstCase: {
      timeline: '5+ років при ігноруванні',
      changes: [
        'Хронічна серцева недостатність',
        'Стійка артеріальна гіпертензія',
        'Значне зниження когнітивних функцій',
        'Висока ймовірність інфаркту або інсульту',
        'Тяжка депресія та тривожність',
        'Зниження якості та тривалості життя'
      ],
      risks: [
        'Ризик раптової смерті при тяжкому апное',
        'Необхідність терапії киснем',
        'Інвалідизація внаслідок інсульту'
      ]
    }
  };
}